package com.thomasrokicki.sensor_data_collector.features.sensor_data_message;

public class SensorDataMessageSample {

	private String sampledAt;
	private String sampleValue;

	public String getSampledAt() {
		return sampledAt;
	}

	public void setSampledAt(String sampledAt) {
		this.sampledAt = sampledAt;
	}

	public String getSampleValue() {
		return sampleValue;
	}

	public void setSampleValue(String sampleValue) {
		this.sampleValue = sampleValue;
	}

}
